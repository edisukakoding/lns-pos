    /**
     * Get all Data format Json from departments
     *
     * @param Request $request
     * @return string
     * @throws JsonException
     */
    public function data(Request $request): string
    {
        $page           = $request->page ?? 1;
        $count          = $request->rows ?? 25;
        return Json::encode([
            'total' => Department::query()->count(),
            'rows' => Department::query()->limit($count)->offset(($page * $count) - $count)->get()->toArray()
        ]);
    }

